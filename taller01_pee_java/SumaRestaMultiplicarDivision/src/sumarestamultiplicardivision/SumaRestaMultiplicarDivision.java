
package sumarestamultiplicardivision;


public class SumaRestaMultiplicarDivision {


    public static void main(String[] args) {


        double num1, num2, suma;

        num1 = 4;
        num2 = 5;

        suma = num1 + num2;

        System.out.println("La suma del numero " + num1 + " con el numero " + num2 + " es: " + suma);
        
        
        double num01, num02, resta;
        num01 = 9;
        num02 = 2;
        
        resta = num01 - num02;
        
        System.out.println("La resta del numero " + num01 + " con el numero " + num02 + " es: " + resta);
        
        
        double nume1, nume2, multiplicacion;
        nume1 = 5;
        nume2 = 2;
        
        multiplicacion = nume1 * nume2;
        
        System.out.println("La multiplicacion del numero " + nume1 + " con el numero " + nume2 + " es: " + multiplicacion);
        
        
        double nume01, nume02, division;
        nume01 = 10;
        nume02 = 2;
        
        division = nume01 / nume02;
        
        System.out.println("La division del numero " + nume01 + " con el numero " + nume02 + " es: " + division);
    }
    
}
